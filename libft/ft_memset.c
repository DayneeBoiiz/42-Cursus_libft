#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	size_t		i;
	char		*p;

	i = 0;
	p = (char *) str;
	while (i < n)
	{
		p[i] = (unsigned char) c;
		i++;
	}
	return (p);
}
